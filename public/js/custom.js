$(document).ready(()=>{
  $("#design-image").click(()=>{
    $("#design-image").hide();
    $("#design").show();
  });
  $("#design").click(()=>{
    $("#design").hide();
    $("#design-image").show();
  });
  $("#development-image").click(()=>{
    $("#development-image").hide();
    $("#development").show();
  });
  $("#development").click(()=>{
    $("#development").hide()
    $("#development-image").show();
  });
  $("#product-image").click(()=>{
    $("#product-image").hide();
    $("#product").show();
  });
  $("#product").click(()=>{
    $("#product").hide();
    $("#product-image").show();
  });
});